    private void viewMatchScoreButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewMatchScoreButtonActionPerformed

        // FROM HERE - Trying to get the display a match method to work.
        ArrayList<String> teamNames = new ArrayList<>();

        // Creating the combo box
        for (Team t : rm.getAllTeams()) {
            teamNames.add(t.getName());
        }

        JComboBox homeTeamComboB = new JComboBox(teamNames.toArray());
        JComboBox awayTeamComboB = new JComboBox(teamNames.toArray());

        // Creates the name input box
        JPanel p = new JPanel();
        p.add(new JLabel("Home Team"));
        p.add(homeTeamComboB);

        p.add(Box.createHorizontalStrut(15));
        p.add(new JLabel("Away Team"));
        p.add(awayTeamComboB);

        int result = JOptionPane.showConfirmDialog(null, p, "Find a match.",
                JOptionPane.OK_CANCEL_OPTION);

        if (result == JOptionPane.OK_OPTION) {
            String ht = homeTeamComboB.getSelectedItem().toString().
                    toLowerCase();
            String at = awayTeamComboB.getSelectedItem().toString().
                    toLowerCase();
            ArrayList<MatchScore> ma = matchLog.getMatch(ht, at);

            // Handles the number of different scenarios when finding a match.
            if (ma == null) { /* No match found */

                JOptionPane.
                        showMessageDialog(null,
                                "No match found with those teams",
                                "Error",
                                JOptionPane.ERROR_MESSAGE);
            } else { /* Only one match found */

                String headers[] = {"Home Team Score", "Away Team Score",
                    "Players"};
                Object[][] data = new Object[ma.size()][3];

                // Put the match details into an object array
                int i = 0;
                for (MatchScore m : ma) {
                    data[i][0] = m.getHomeTWin();
                    data[i][1] = m.getAwayTWin();
                    data[i][2] = m.getHomeTeam().getPlayers().toString();
                    i++;
                }

                // Creates and displays the table.
                JTable table = new JTable(data, headers) {
                    public boolean isCellEditable(int row, int column) {
                        return false;
                    }
                };

                JScrollPane scrollPane = new JScrollPane(table);
                table.setFillsViewportHeight(true);

                JFrame frame = new JFrame();

                frame.add(scrollPane, BorderLayout.CENTER);
                frame.setSize(500, 250);
                frame.setVisible(true);

            }
        }

    }//GEN-LAST:event_viewMatchScoreButtonActionPerformed

