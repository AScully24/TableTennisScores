    private void newMatchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_newMatchButtonActionPerformed
        boolean pValidArray[] = new boolean[8];
        String names[] = new String[8];
        ArrayList<String> wrongNames = new ArrayList();

        this.aTeamNameString = aTeamField.getText();
        this.hTeamNameString = hTeamField.getText();

        // Checking that team names are correct.
        boolean isInvalidTeam = false;

        ArrayList<String> teamNameErrors = new ArrayList<>();

        if (rm.getTeam(hTeamNameString) == null) {
            isInvalidTeam = true;
            teamNameErrors.add(hTeamNameString);
        }

        if (rm.getTeam(aTeamNameString) == null) {
            isInvalidTeam = true;
            teamNameErrors.add(aTeamNameString);
        }

        // Displays error message and exits if one isn't found
        if (isInvalidTeam) {
            String errMsg = "You have entered invalid team name(s): \n";
            for (String s : teamNameErrors) {
                errMsg += s + "\n";
            }

            JOptionPane.showMessageDialog(this, errMsg, "Team Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Checking player names
        names[0] = this.hp1 = hPlayer1.getText().toLowerCase();
        names[1] = this.hp2 = hPlayer2.getText().toLowerCase();
        names[2] = this.ap1 = aPlayer1.getText().toLowerCase();
        names[3] = this.ap2 = aPlayer2.getText().toLowerCase();
        names[4] = this.ap2 = hdblplayer1.getText().toLowerCase();
        names[5] = this.ap2 = hdblplayer2.getText().toLowerCase();
        names[6] = this.ap2 = adblplayer1.getText().toLowerCase();
        names[7] = this.ap2 = adblplayer2.getText().toLowerCase();

        // Has to be in the same order as the names array.
        pValidArray[0] = rm.getTeam(hTeamNameString).isPlayerValid(names[0]);
        pValidArray[1] = rm.getTeam(hTeamNameString).isPlayerValid(names[1]);
        pValidArray[2] = rm.getTeam(aTeamNameString).isPlayerValid(names[2]);
        pValidArray[3] = rm.getTeam(aTeamNameString).isPlayerValid(names[3]);
        pValidArray[4] = rm.getTeam(hTeamNameString).isPlayerValid(names[4]);
        pValidArray[5] = rm.getTeam(hTeamNameString).isPlayerValid(names[5]);
        pValidArray[6] = rm.getTeam(aTeamNameString).isPlayerValid(names[6]);
        pValidArray[7] = rm.getTeam(aTeamNameString).isPlayerValid(names[7]);

        // Default false. True when a play is invalid
        boolean isInvalidPlayer = false;

        for (int i = 0; i < pValidArray.length; i++) {
            boolean b = pValidArray[i];
            if (!b) {
                isInvalidPlayer = true;
                wrongNames.add(names[i]);
            }
        }

        if (isInvalidPlayer) {
            String errMsg = "You have entered invalid players: \n";
            for (String s : wrongNames) {
                errMsg += s + "\n";
            }

            JOptionPane.showMessageDialog(this, errMsg, "Player Error",
                    JOptionPane.ERROR_MESSAGE);
        }

        // If the teams and the names are correct, the user can continue.
        if (!isInvalidTeam && !isInvalidPlayer) {
            calScorebutton.setEnabled(true);

            hPlayer1.setEnabled(false);
            hPlayer2.setEnabled(false);
            aPlayer1.setEnabled(false);
            aPlayer2.setEnabled(false);
            hdblplayer1.setEnabled(false);
            hdblplayer2.setEnabled(false);
            adblplayer1.setEnabled(false);
            adblplayer2.setEnabled(false);

            hTeamField.setEnabled(false);
            aTeamField.setEnabled(false);
        }


    }//GEN-LAST:event_newMatchButtonActionPerformed

