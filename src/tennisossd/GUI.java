// Handles importing teams and players
    public void teamAndPlayerImporter() {

        // IMPORTS TEAMS
        Team t = null;
        Scanner scData = new Scanner(Test.class.getResourceAsStream(
                "/team_data.txt"));

        scData.nextLine();
        while (scData.hasNextLine()) {
            String lineData[] = scData.nextLine().split(",");
            t = new Team(lineData[0], lineData[1]);
            t.addPoints(Integer.parseInt(lineData[2]));
            rm.addTeam(t);
        }

        scData.close();
        // IMPORTS PLAYERS
        t = null;

        scData = new Scanner(Test.class.getResourceAsStream("/player_data.txt"));
        scData.nextLine();
        while (scData.hasNextLine()) {
            String lineData[] = scData.nextLine().split(",");
            Player p = new Player(lineData[0]);
            p.setSetsWons(Integer.parseInt(lineData[1]));
            p.setSetsPlayed(Integer.parseInt(lineData[2]));
            t = new Team(lineData[3], lineData[3]);
            rm.regPlayerToTeam(p, t);
        }
        scData.close();
    }

